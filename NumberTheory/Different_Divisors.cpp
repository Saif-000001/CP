bool isPrime(ll n)
{
    if (n == 1)
        return false;
    for (int i = 2; i * i <= n; i++)
    {
        if (n % i == 0)
            return false;
    }
    return true;
}

void TEST_CASES()
{
    ll d;
    cin >> d;
    ll x, y;
    x = d + 1;
    while (!isPrime(x))
        x++;
    y = d + x;
    while (!isPrime(y))
        y++;
    cout << (x * y) << "\n";
}

